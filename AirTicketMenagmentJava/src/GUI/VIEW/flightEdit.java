    public void autoID(){
        try {
            Class.forName("com.mysql.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost/airticketmenagment","root","");
            Statement s = con.createStatement();
            ResultSet rs= s.executeQuery("select MAX(flightID) from flight");
            rs.next();
            rs.getString("MAX(flightID)");
            if(rs.getString("MAX(flightID)")== null){
                txtflightid.setText("F0001");
            }else{
                long passengerID = Long.parseLong(rs.getString("MAX(flightID)").substring(2,rs.getString("MAX(flightID)").length()));
               passengerID++;
               txtflightid.setText("F"+ String.format("%03d", passengerID));
                
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddPassenger.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddPassenger.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    
