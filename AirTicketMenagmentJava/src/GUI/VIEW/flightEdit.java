    private void addflightbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addflightbtnActionPerformed
         String flId = txtflightid.getText();
         String flName = txtflifhtname.getText();
          String flsource = txtflightsource.getText();
           String fldep = txtflightdep.getText();
            String flcharge = txtflightcharge.getText();
            DateFormat da = new SimpleDateFormat("MM-dd-yyyy");
            String date =da.format(datachooserflight.getDate());
            
           
           String fldeptime = txtflightdeptime.getText();
           String flarrtime = txtflightarrtime.getText();
        
        try {
            Class.forName("com.mysql.jdbc.Driver");
              con = DriverManager.getConnection("jdbc:mysql://localhost/airticketmenagment","root","");
              pst =con.prepareStatement("insert into flight(flightID,flightName,flightSource,flightDepart,flightDate,flightCharge,fldeptime,flarrtime)value(?,?,?,?,?,?,?,?)");
              pst.setString(1, flId);
              pst.setString(2, flName);
              pst.setString(3, flsource);
              pst.setString(4, fldep);
              pst.setString(5, date);
              pst.setString(6, flcharge);
              pst.setString(7, fldeptime);
              pst.setString(8,  flarrtime );
           
              pst.executeUpdate();
              JOptionPane.showMessageDialog(null, "Flight Created");
              
              
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(AddPassenger.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(AddPassenger.class.getName()).log(Level.SEVERE, null, ex);
        }
          
        
        
        
        
        
        
        
        
    }//GEN-LAST:event_addflightbtnActionPerformed

